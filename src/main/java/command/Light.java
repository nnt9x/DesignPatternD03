package command;

public class Light {

    // Gồm có 2 phương thức: On, off
    public void on()
    {
        System.out.println("Light is on");
    }
    public void off()
    {
        System.out.println("Light is off");
    }
    public void dimLight(){
        System.out.println("Light is dim");
    }
}
